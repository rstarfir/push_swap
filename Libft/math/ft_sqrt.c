#include "libft.h"

int		ft_sqrt(int nb)
{
	int sq;

	sq = 0;
	if (nb > 0)
	{
		while (sq <= (nb / 2))
		{
			if ((sq * sq) == nb)
			{
				return (sq);
			}
			sq++;
		}
	}
	return (0);
}
